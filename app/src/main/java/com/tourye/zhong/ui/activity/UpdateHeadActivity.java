    @Override
    protected void onActivityResult(int requestCode, int resultCode, Intent data) {
        super.onActivityResult(requestCode, resultCode, data);
        if (resultCode == RESULT_OK) {
            switch (requestCode) {
                case PictureConfig.CHOOSE_REQUEST:
                    // 图片选择结果回调
                    selectList.clear();
                    selectList = PictureSelector.obtainMultipleResult(data);
                    // 例如 LocalMedia 里面返回三种path
                    // 1.media.getPath(); 为原图path
                    // 2.media.getCutPath();为裁剪后path，需判断media.isCut();是否为true
                    // 3.media.getCompressPath();为压缩后path，需判断media.isCompressed();是否为true
                    // 如果裁剪并压缩了，已取压缩路径为准，因为是先裁剪后压缩的
                    for (LocalMedia media : selectList) {
                        Log.i("图片-----》", media.getPath());
                    }
                    if (selectList!=null && selectList.get(0)!=null) {
                        String path = "";
                        LocalMedia media = selectList.get(0);
                        if (media.isCut() && !media.isCompressed()) {
                            // 裁剪过
                            path = media.getCutPath();
                        } else if (media.isCompressed() || (media.isCut() && media.isCompressed())) {
                            // 压缩过,或者裁剪同时压缩过,以最终压缩过图片为准
                            path = media.getCompressPath();
                        } else {
                            // 原图
                            path = media.getPath();
                        }
                        RequestOptions requestOptions=new RequestOptions();
                        requestOptions.centerCrop()
                                .placeholder(R.color.color_f6)
                                .diskCacheStrategy(DiskCacheStrategy.ALL);
                        Glide.with(BaseApplication.mApplicationContext)
                                .load(path)
                                .apply(requestOptions)
                                .into(mImgActivityUpdateHead);
                    }
                    break;
            }
        }
    }


    /**
     * 拍照对话框
     */
    public void showCameraDialog() {
        ModifyHeadDialog modifyHeadDialog = new ModifyHeadDialog(this);
        modifyHeadDialog.setCameraCallback(new ModifyHeadDialog.CameraCallback() {
            @Override
            public void openCamera() {
                goCamera();
            }

            @Override
            public void openGallery() {

                goAlbum();
            }
        });
        modifyHeadDialog.show();
    }

    @Override
    public void onClick(View view) {
        switch (view.getId()) {
            case R.id.tv_activity_update_head_close:
                finish();
                break;
            case R.id.img_activity_update_head_camera:
//                requestPermission();
                requestPermissionRx();
                break;
        }

    }
